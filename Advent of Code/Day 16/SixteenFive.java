    public static int findBestPaths() {
        Point starting = findStartingLocation();

        Map<String, Integer> visited = new HashMap<>();

        PriorityQueue<Path> queue = new PriorityQueue<>(Comparator.comparingInt(points -> points.total));

        queue.add(new Path(starting.x, starting.y, 1, 0));
        Set<Point> optimalTiles = new HashSet<>();
        // List<Path> allBestPaths = new ArrayList<>();

        Integer bestPath = null;

        while (!queue.isEmpty()) {
            Path current = queue.poll();

            // If we already know the minimum score and this path is worse, skip it
            if (bestPath != null && current.total > bestPath) {
                continue;
            }

            String pathKey = current.x + ", " + current.y + ", " + current.direction;

            if (visited.containsKey(pathKey) && visited.get(pathKey) <= current.total) {
                // for (Point point: current.path) {
                //     if (point != null) {
                //         System.err.println(point.toString());
                //     }
                // }
                continue;
            }

            visited.put(pathKey, current.total);

            int newX = current.x + DIRECTIONS[current.direction].x;
            int newY = current.y + DIRECTIONS[current.direction].y;

            if (nextStepValid(newX, newY)) {
                // Check if end reached
                if (maze[newY][newX] == 'E') {
                    int finalScore = current.total + 1;
                    // allBestPaths.add(new Path(current.x, current.y, current.direction, current.total, current.path));
                    
                    if (bestPath == null || finalScore == bestPath) {
                        bestPath = finalScore;

                        optimalTiles.addAll(current.path);
                        optimalTiles.add(new Point(newX, newY)); // Add the end point
                    }
                } else {
                    queue.add(new Path(newX, newY, current.direction, current.total + 1, current.path));
                }
            }

            int leftDirection = (current.direction + 3) % 4;
            queue.add(new Path(current.x, current.y, leftDirection, current.total + 1000, current.path));


            int rightDirection = (current.direction + 1) % 4;
            queue.add(new Path(current.x, current.y, rightDirection, current.total + 1000, current.path));
        }

        return optimalTiles.size();
    }


    private static boolean nextStepValid(int x, int y) {
        return maze[y][x] != '#';
    }

    private static void visualisePath(Set<Point> successfulPath) {
        char[][] path = maze.clone();

        for (Point coordinate: successfulPath) {
            path[coordinate.y][coordinate.x] = 'X';
        }

        for (char[] line: path) {
            System.err.println(line);
        }
        System.err.println("");
    }

